package it.unibo.javapoly.model.api.economy;

/**
 * Enumeration representing the different types of financial
 * transaction that can occur during a game.
 */
public enum TransactionType {
    BUY_PROPERTY,
    SELL_PROPERTY,
    BUY_HOUSE,
    SELL_HOUSE,
    PAY_RENT,
    WITHDRAW_TO_BANK,
    DEPOSIT_FROM_BANK
}
